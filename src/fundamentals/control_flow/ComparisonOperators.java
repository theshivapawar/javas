package fundamentals.control_flow;

public class ComparisonOperators {
    public static void main(String[] args) {

        // Used to compare primitive values

        System.out.println(1 == 1); // Equality
        System.out.println(10 > 9);
        System.out.println(67 < 37);
        System.out.println(44 <= 11);
        System.out.println(56 >= 10);

        int number1 = 2;
        int number2 = 2;
        System.out.println(number1 != number2); // Inequality
    }
}